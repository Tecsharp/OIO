
public class Sonido {
	
	private String marcaSonido;
	private String numeroBocinas;
	private String tipoControl;
	
	
	public String getMarcaSonido() {
		return marcaSonido;
	}
	public void setMarcaSonido(String marcaSonido) {
		this.marcaSonido = marcaSonido;
	}
	public String getNumeroBocinas() {
		return numeroBocinas;
	}
	public void setNumeroBocinas(String numeroBocinas) {
		this.numeroBocinas = numeroBocinas;
	}
	public String getTipoControl() {
		return tipoControl;
	}
	public void setTipoControl(String tipoControl) {
		this.tipoControl = tipoControl;
	}
	

}
