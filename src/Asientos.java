
public class Asientos {

	private String materialAsiento; //cuero o sintetico
	private String tipoAsientos; //Electricos
	private String equipoAsiento; //Calefaccion
	
	public String getMaterialAsiento() {
		return materialAsiento;
	}
	public void setMaterialAsiento(String materialAsiento) {
		this.materialAsiento = materialAsiento;
	}
	public String getTipoAsientos() {
		return tipoAsientos;
	}
	public void setTipoAsientos(String tipoAsientos) {
		this.tipoAsientos = tipoAsientos;
	}
	public String getEquipoAsiento() {
		return equipoAsiento;
	}
	public void setEquipoAsiento(String equipoAsiento) {
		this.equipoAsiento = equipoAsiento;
	}
	
}
