package vehiculo.partes;

public class Volante {
	
	private String materialVolante; //cuero o sintetico
	private String tipoVolante; //deportivo o normal
	private String volanteTamano;
	
	
	public String getMaterialVolante() {
		return materialVolante;
	}
	public void setMaterialVolante(String materialVolante) {
		this.materialVolante = materialVolante;
	}
	public String getTipoVolante() {
		return tipoVolante;
	}
	public void setTipoVolante(String tipoVolante) {
		this.tipoVolante = tipoVolante;
	}
	public String getVolanteTamano() {
		return volanteTamano;
	}
	public void setVolanteTamano(String volanteTamano) {
		this.volanteTamano = volanteTamano;
	}
	

}
