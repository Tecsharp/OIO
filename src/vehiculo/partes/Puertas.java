package vehiculo.partes;

public class Puertas {
	
	private String tipoVidrios;
	private String tipoPuerta;
	private String dimPuertasDelanteras;
	private String dimPuertrasTraseras;
	
	
	
	public String getTipoVidrios() {
		return tipoVidrios;
	}
	public void setTipoVidrios(String tipoVidrios) {
		this.tipoVidrios = tipoVidrios;
	}
	public String getTipoPuerta() {
		return tipoPuerta;
	}
	public void setTipoPuerta(String tipoPuerta) {
		this.tipoPuerta = tipoPuerta;
	}
	public String getDimPuertasDelanteras() {
		return dimPuertasDelanteras;
	}
	public void setDimPuertasDelanteras(String dimPuertasDelanteras) {
		this.dimPuertasDelanteras = dimPuertasDelanteras;
	}
	public String getDimPuertrasTraseras() {
		return dimPuertrasTraseras;
	}
	public void setDimPuertrasTraseras(String dimPuertrasTraseras) {
		this.dimPuertrasTraseras = dimPuertrasTraseras;
	}

	
	

}
